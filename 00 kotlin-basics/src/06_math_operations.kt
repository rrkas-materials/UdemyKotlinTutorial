fun main() {
    print("n1: ")
    val n1 = readLine()!!.toDouble()
    print("n2: ")
    val n2 = readLine()!!.toDouble()
    println("n1 = $n1")
    println("n2 = $n2")
    println("Sum = ${n1 + n2}")
    println("Diff = ${n1 - n2}")
    println("Prod = ${n1 * n2}")
    println("Quot = ${n1 / n2}")
    println("Mod = ${n1 % n2}")
}